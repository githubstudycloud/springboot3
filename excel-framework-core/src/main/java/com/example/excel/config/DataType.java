package com.example.excel.config;

/**
 * 数据类型枚举
 */
public enum DataType {
    /**
     * 字符串类型
     */
    STRING,
    
    /**
     * 整数类型
     */
    INTEGER,
    
    /**
     * 长整数类型
     */
    LONG,
    
    /**
     * 双精度浮点数类型
     */
    DOUBLE,
    
    /**
     * 布尔类型
     */
    BOOLEAN,
    
    /**
     * 日期类型
     */
    DATE,
    
    /**
     * 日期时间类型
     */
    DATETIME,
    
    /**
     * 大数字类型
     */
    BIG_DECIMAL
}
